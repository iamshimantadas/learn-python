# remove items using append() method
lst = [10,60,55,-90,400]
print(lst)
lst.remove(55)
print(lst)

# remove items using pop() method
lst2 = ["tez","cypher","dante"]
print(lst2)
lst2.pop()
print(lst2)

# remove element using 'del' keyword
lst3 = [12.4,34.5,67.8,90.89]
print(lst3)
del lst3[1]
print(lst3)